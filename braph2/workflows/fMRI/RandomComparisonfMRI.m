    methods (Access=protected)  % Initialize data
        function initialize_data(rc, varargin)
            atlases = rc.getBrainAtlases();
            atlas = atlases{1};   
            group = rc.getGroup();  % randomgroup should have the same dims as group.
                        
            measure_code = rc.getMeasureCode();
            
            number_of_permutations = rc.getSettings('RandomComparisonfMRI.PermutationNumber');
            
            if Measure.is_global(measure_code)  % global measure
                % values
                rc.value_1 = get_from_varargin( ...
                    repmat({0}, 1, group.subjectnumber()), ...
                    'RandomComparisonfMRI.value_1', ...
                    varargin{:});
                rc.value_2 = get_from_varargin( ...
                    repmat({0}, 1, group.subjectnumber()), ...
                    'RandomComparisonfMRI.value_2', ...
                    varargin{:});
                assert(iscell(rc.getGroupValue(1)) & ...
                    isequal(size(rc.getGroupValue(1)), [1, group.subjectnumber()]) & ...
                    all(cellfun(@(x) isequal(size(x), [1, 1]), rc.getGroupValue(1))), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])  %#ok<*NBRAK>
                assert(iscell(rc.getGroupValue(2)) & ...
                    isequal(size(rc.getGroupValue(2)), [1, group.subjectnumber()]) & ...
                    all(cellfun(@(x) isequal(size(x), [1, 1]), rc.getGroupValue(2))), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                
                % average values
                rc.average_value_1 = get_from_varargin( ...
                    0, ...
                    'RandomComparisonfMRI.average_value_1', ...
                    varargin{:});
                rc.average_value_2 = get_from_varargin( ...
                    0, ...
                    'RandomComparisonfMRI.average_value_2', ...
                    varargin{:});
                assert(isequal(size(rc.getGroupAverageValue(1)), [1, 1]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getGroupAverageValue(2)), [1, 1]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                
                % statistic measures
                rc.difference = get_from_varargin( ...
                    0, ...
                    'RandomComparisonfMRI.difference', ...
                    varargin{:});
                rc.all_differences = get_from_varargin( ...
                    repmat({0}, 1, number_of_permutations), ...
                    'RandomComparisonfMRI.all_differences', ...
                    varargin{:});
                rc.p1 = get_from_varargin( ...
                    0, ...
                    'RandomComparisonfMRI.p1', ...
                    varargin{:});
                rc.p2 = get_from_varargin( ...
                    0, ...
                    'RandomComparisonfMRI.p2', ...
                    varargin{:});
                rc.confidence_interval_min = get_from_varargin( ...
                    0, ...
                    'RandomComparisonfMRI.confidence_min', ...
                    varargin{:});
                rc.confidence_interval_max = get_from_varargin( ...
                    0, ...
                    'RandomComparisonfMRI.confidence_max', ...
                    varargin{:});
                
                assert(isequal(size(rc.getDifference()), [1, 1]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getAllDifferences()), [1, number_of_permutations]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getP1()), [1, 1]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getP2()), [1, 1]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                
            elseif Measure.is_nodal(measure_code)  % nodal measure
                rc.value_1 = get_from_varargin( ...
                    repmat({zeros(atlas.getBrainRegions().length(), 1)}, 1, group.subjectnumber()), ...
                    'RandomComparisonfMRI.value_1', ...
                    varargin{:});
                rc.value_2 = get_from_varargin( ...
                    repmat({zeros(atlas.getBrainRegions().length(), 1)}, 1, group.subjectnumber()), ...
                    'RandomComparisonfMRI.value_2', ...
                    varargin{:});
                assert(iscell(rc.getGroupValue(1)) & ...
                    isequal(size(rc.getGroupValue(1)), [1, group.subjectnumber()]) & ...
                    all(cellfun(@(x) isequal(size(x), [atlas.getBrainRegions().length(), 1]), rc.getGroupValue(1))), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(iscell(rc.getGroupValue(2)) & ...
                    isequal(size(rc.getGroupValue(2)), [1, group.subjectnumber()]) & ...
                    all(cellfun(@(x) isequal(size(x), [atlas.getBrainRegions().length(), 1]), rc.getGroupValue(2))), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                
                rc.average_value_1 = get_from_varargin( ...
                    zeros(atlas.getBrainRegions().length(), 1), ...
                    'RandomComparisonfMRI.average_value_1', ...
                    varargin{:});
                rc.average_value_2 = get_from_varargin( ...
                    zeros(atlas.getBrainRegions().length(), 1), ...
                    'RandomComparisonfMRI.average_value_2', ...
                    varargin{:});
                assert(isequal(size(rc.getGroupAverageValue(1)), [atlas.getBrainRegions().length(), 1]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getGroupAverageValue(2)), [atlas.getBrainRegions().length(), 1]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                
                % statistic values
                rc.difference = get_from_varargin( ...
                    zeros(atlas.getBrainRegions().length(), 1), ...
                    'RandomComparisonfMRI.difference', ...
                    varargin{:});
                rc.all_differences = get_from_varargin( ...
                    repmat({zeros(atlas.getBrainRegions().length(), 1)}, 1, number_of_permutations), ...
                    'RandomComparisonfMRI.all_differences', ...
                    varargin{:});
                rc.p1 = get_from_varargin( ...
                    zeros(atlas.getBrainRegions().length(), 1), ...
                    'RandomComparisonfMRI.p1', ...
                    varargin{:});
                rc.p2 = get_from_varargin( ...
                    zeros(atlas.getBrainRegions().length(), 1), ...
                    'RandomComparisonfMRI.p2', ...
                    varargin{:});
                rc.confidence_interval_min = get_from_varargin( ...
                    zeros(atlas.getBrainRegions().length(), 1), ...
                    'RandomComparisonfMRI.confidence_min', ...
                    varargin{:});
                rc.confidence_interval_max = get_from_varargin( ...
                    zeros(atlas.getBrainRegions().length(), 1), ...
                    'RandomComparisonfMRI.confidence_max', ...
                    varargin{:});
                
                assert(isequal(size(rc.getDifference()), [atlas.getBrainRegions().length(), 1]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getAllDifferences()), [1, number_of_permutations]), ...  % it should be like this currently the second dimension is expanding depending on modality.
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getP1()), [atlas.getBrainRegions().length(), 1]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getP2()), [atlas.getBrainRegions().length(), 1]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getConfidenceIntervalMin()), [atlas.getBrainRegions().length(), 1]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getConfidenceIntervalMax()), [atlas.getBrainRegions().length(), 1]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                
            elseif Measure.is_binodal(measure_code)  % binodal measure
                rc.value_1 = get_from_varargin( ...
                    repmat({zeros(atlas.getBrainRegions().length())}, 1, group.subjectnumber()), ...
                    'RandomComparisonfMRI.value_1', ...
                    varargin{:});
                rc.value_2 = get_from_varargin( ...
                    repmat({zeros(atlas.getBrainRegions().length())}, 1, group.subjectnumber()), ...
                    'RandomComparisonfMRI.value_2', ...
                    varargin{:});
                assert(iscell(rc.getGroupValue(1)) & ...
                    isequal(size(rc.getGroupValue(1)), [1, group.subjectnumber()]) & ...
                    all(cellfun(@(x) isequal(size(x), [atlas.getBrainRegions().length(), atlas.getBrainRegions().length()]), rc.getGroupValue(1))), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(iscell(rc.getGroupValue(2)) & ...
                    isequal(size(rc.getGroupValue(2)), [1, group.subjectnumber()]) & ...
                    all(cellfun(@(x) isequal(size(x), [atlas.getBrainRegions().length(), atlas.getBrainRegions().length()]), rc.getGroupValue(2))), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                
                rc.average_value_1 = get_from_varargin( ...
                    zeros(atlas.getBrainRegions().length()), ...
                    'RandomComparisonfMRI.average_value_1', ...
                    varargin{:});
                rc.average_value_2 = get_from_varargin( ...
                    zeros(atlas.getBrainRegions().length()), ...
                    'RandomComparisonfMRI.average_value_2', ...
                    varargin{:});
                assert(isequal(size(rc.getGroupAverageValue(1)), [atlas.getBrainRegions().length(), atlas.getBrainRegions().length()]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getGroupAverageValue(2)), [atlas.getBrainRegions().length(), atlas.getBrainRegions().length()]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                
                % statistic values
                rc.difference = get_from_varargin( ...
                    zeros(atlas.getBrainRegions().length()), ...
                    'RandomComparisonfMRI.difference', ...
                    varargin{:});
                rc.all_differences = get_from_varargin( ...
                    repmat({zeros(atlas.getBrainRegions().length())}, 1, number_of_permutations), ...
                    'RandomComparisonfMRI.all_differences', ...
                    varargin{:});
                rc.p1 = get_from_varargin( ...
                    zeros(atlas.getBrainRegions().length()), ...
                    'RandomComparisonfMRI.p1', ...
                    varargin{:});
                rc.p2 = get_from_varargin( ...
                    zeros(atlas.getBrainRegions().length()), ...
                    'RandomComparisonfMRI.p2', ...
                    varargin{:});
                rc.confidence_interval_min = get_from_varargin( ...
                    zeros(atlas.getBrainRegions().length()), ...
                    'RandomComparisonfMRI.confidence_min', ...
                    varargin{:});
                rc.confidence_interval_max = get_from_varargin( ...
                    zeros(atlas.getBrainRegions().length()), ...
                    'RandomComparisonfMRI.confidence_max', ...
                    varargin{:});
                
                assert(isequal(size(rc.getDifference()), [atlas.getBrainRegions().length(), atlas.getBrainRegions().length()]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getAllDifferences()), [1, number_of_permutations]), ...  % it should be like this currently the second dimension is expanding depending on modality.
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getP1()), [atlas.getBrainRegions().length(), atlas.getBrainRegions().length()]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getP2()), [atlas.getBrainRegions().length(), atlas.getBrainRegions().length()]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getConfidenceIntervalMin()), [atlas.getBrainRegions().length(), atlas.getBrainRegions().length()]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
                assert(isequal(size(rc.getConfidenceIntervalMax()), [atlas.getBrainRegions().length(), atlas.getBrainRegions().length()]), ...
                    ['BRAPH:RandomComparisonfMRI:WrongData'], ...
                    ['Data not compatible with RandomComparisonfMRI.'])
            end
        end
